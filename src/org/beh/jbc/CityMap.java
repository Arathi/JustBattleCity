	public void loadFromROM(int stageId){
		int x, y, hx, rawIndex, mapIndex;
		int offsetStart=((stageId-1)%35)*91;
		byte data;
		for (y=0;y<13;y++){
			for (hx=0; hx<7; hx++){
				rawIndex = hx+y*7;
				data = romMapData[offsetStart+rawIndex];
				x = hx*2;
				mapIndex = x+y*13;
				mapdata[mapIndex] = (byte) ((data>>4) & 0x0F);
				if (hx!=6){
					x = hx*2+1;
					mapIndex = x+y*13;
					mapdata[mapIndex] = (byte) (data & 0x0F);
				}
			}
		}
	}
	
	public void loadFromFile(String path){
		if (path==null || path.trim().equals("")){
			loadFromROM(1);
		}
	}
	
